export function matrixBySlug(slug) {
  return API_TEST_MATRIX.find((a) => a.slug === slug.toLowerCase());
}

export function filterMatrix({ slug } = {}) {
  if (slug) {
    const one = matrixBySlug(slug);
    return one ? [one] : [];
  }
  return API_TEST_MATRIX;
}

export function diffMatrixVsCatalog(catalogSlugs) {
  const matrixSlugs = new Set(API_TEST_MATRIX.map((a) => a.slug));
  const catalog = new Set(catalogSlugs.map((s) => s.toLowerCase()));
  return {
    missingFromMatrix: [...catalog].filter((s) => !matrixSlugs.has(s)),
    extraInMatrix: [...matrixSlugs].filter((s) => !catalog.has(s)),
    matrixCount: matrixSlugs.size,
    catalogCount: catalog.size,
  };
}

export function isSoftRunFailure(status, data) {
  if (status === 402 || status === 428) return true;
  const msg = JSON.stringify(data ?? "").toLowerCase();
  return (
    msg.includes("insufficient credit") ||
    msg.includes("not connected") ||
    msg.includes("integration")
  );
}

export function slugInContent(slug, content) {
  const c = content.toLowerCase();
  return c.includes(slug.toLowerCase()) || c.includes(slug.replace(/-/g, " "));
}
